def test_maxprod_composition():
    m = np.array([
        [1.0, 0.6, 0.9, 0],
        [0.6, 1.0, 0.5, 0.9],
        [0.9, 0.5, 1.0, 0.9],
        [0, 0.9, 0.9, 1.0]
    ])
    m_max_product_2 = np.array([
        [1.0, 0.6, 0.9, 0.81],
        [0.6, 1.0, 0.81, 0.9],
        [0.9, 0.81, 1.0, 0.9],
        [0.81, 0.9, 0.9, 1.0]
    ])
    m_max_product_3 = np.array([
        [1.0, 0.729, 0.9, 0.81],
        [0.729, 1.0, 0.81, 0.9],
        [0.9, 0.81, 1.0, 0.9],
        [0.81, 0.9, 0.9, 1.0]
    ])

    # Test composition
    assert np.allclose(k_maxprod_composition(m), m_max_product_2)
    assert np.allclose(k_maxprod_composition(m, 3), m_max_product_3)


def test_maxprod_transitivity():
    m_non_transitive = np.array([
        [1.0, 0.6, 0.9, 0],
        [0.6, 1.0, 0.5, 0.9],
        [0.9, 0.5, 1.0, 0.9],
        [0, 0.9, 0.9, 1.0]
    ])
    n_transitive = np.array([
        [1.0, 0.8, 0.5],
        [0.0, 1.0, 0.6],
        [0.0, 0.0, 1.0]
    ])

    # Test transitivity
    assert not is_maxprod_transitive(m_non_transitive)
    assert is_maxprod_transitive(n_transitive)

    # Test closure
    assert is_maxprod_transitive(maxprod_transitive_closure(m_non_transitive))


